    <p>Сразу видно, когда кошка хорошо себя чувствует. Она интересуется окружающим миром, ест, играет, расслаблено спит на спине, хочет общаться с людьми и другими кошками. А как же понять, что животному плохо?
    Поскольку кошки скрывают боль и плохое самочувствие, далеко не всегда они будут как-то жаловаться на свое состояние. Понять, что кошке плохо, можно по ее внешнему виду и по тому, как она себя ведет.</p>
    <div class="list-1"> 
    <h2>Здоровое состояние</h2>
    <ul class="healthy-list">
      <li class="healthy-item">Уши направлены вперед</li>
      <li class="healthy-item">Глаза раскрыты</li>
      <li class="healthy-item">Пасть расслаблена</li>
      <li class="healthy-item">Усы распушены, кончики <br> смотрят вниз</li>
      <li class="healthy-item">Голова выше линии плеч</li> </ul>
    </div>
    <div class="list-2">
    <h2 class="pain-h2">Умеренная боль</h2>
    <ul class="pain-list">
      <li class="pain-item">Уши слегка развернуты <br> и немножко опущены</li>
      <li class="pain-item">Глаза прикрыты</li>
      <li class="pain-item">Пасть напряжена</li>
      <li class="pain-item">Усы направлены <br> горизонтально<br> или немного <br> сгибаются вниз </li>
      <li class="pain-item">Голова на уровне плеч</li>
    </ul>
  </div>
  <div class="list-3">
    <h2 class="strong-pain-h2">Сильная боль</h2>
    <ul class="strong-list">
      <li class="strong-item">Уши развернуты назад <br> и опущены</li>
      <li class="strong-item">Кошка щурится</li>
      <li class="strong-item">Пасть сильно напряжена</li>
      <li class="strong-item">Усы направлены вперед <br> от морды </li>
      <li class="strong-item">Голова ниже уровня плеч <br> или наклонена вниз <br> (подбородок у груди)</li>
    </ul>
  </div>
