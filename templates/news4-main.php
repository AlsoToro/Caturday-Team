      <h2> Периоды жизни кошки</h2>
      <h3>Младенчество (0-4 недель)</h3>
    <p> В это время котёнок беспомощен, его глаза открываются на 10 сутки, ползать он начинает на 5-й день, а уверенно ходить — к 3-4 неделе.</p>
      <img src="img/little-kitten.jpg" width="720px" height="501px" alt="Котенок новорожденный">
      <p class='source-img'> Источник:<a class="link" href="https://chudo-prirody.com/koshki/630-novorozhdennye-kotjata-208-foto.html">https://chudo-prirody.com/koshki/630-novorozhdennye-kotjata-208-foto.html</a></p>
     <p>Опорожниться малыш сам не может, ему помогает кошка, вылизывая ему промежность и массируя живот языком.</p>
      
     <h3> Детство (1-6 месяцев) </h3>
     <img src="img/kitten.jpeg" width="720px" height="480px" alt="Котенок">
     <p class='source-img'> Источник:<a class="link" href="https://ru.wallpaper.mob.org/image/animal-cat-baby_animal-kitten-483700.html">https://ru.wallpaper.mob.org/image/animal-cat-baby_animal-kitten-483700.html</a></p>
     <p>Самый любопытный и милый возраст у котиков.
      Именно в этот период им придётся постигнуть всё то, что умеет кошка:
      научиться вылизываться;
      освоить лоток;
      попробовать твёрдую пищу;
      научиться лакать воду;
      молочные зубки заменяются на постоянные.
      Котята социализируются в период детства, специалисты считают, что "окно социализации" закрывается именно к 5-6 месяцам жизни кошки.</p>
      
    <h3>  Подростковость (7-12 месяцев)</h3>
      В эту пору котята-подростки становятся половозрелыми.
      <img src="img/teenage.jpg" width="720px" height="541px" alt="Котенок-подросток">
      <p class='source-img'> Источник:<a class="link" href="http://helpcat.ru/index.php/eto-vazhno-znat-2/item/1123-fanta">http://helpcat.ru/index.php/eto-vazhno-znat-2/item/1123-fanta</a></p>
      Коты начинают метить территорию, а самочки страдать от первой течки.
      
     <h3> Юность (1-2 года)</h3>
      В это время питомцы уже могут заводить потомство. Однако зрелость зависит от породы.
      <img src="img/cat-1year.jpg" width="720px" height="480px" alt="Юный кот">
      <p class='source-img'> Источник:<a class="link" href="https://pixnio.com/ru/животных/кот/серый-кот-весна-зеленая-трава-милый-п">https://pixnio.com/ru/животных/кот/серый-кот-весна-зеленая-трава-милый-п</a></p>
      Например, мейн-куны и бобтейлы формируются к 2-3 годам, когда как шотландцы и сиамы — уже к году.
      
     <h3> Взрослая кошка (3-7 лет)</h3>
      Животное в этом возрасте полно сил и энергии. Это — расцвет жизни кошки.
      <img src="img/adult-cat.jpeg" width="720px" height="540px" alt="Взрослый кот">
      <p class='source-img'> Источник:<a class="link" href="https://fun-cats.ru/cauzi-opisanie-porody-kosek-harakter-zdorove-s-foto-i-video/">https://fun-cats.ru/cauzi-opisanie-porody-kosek-harakter-zdorove-s-foto-i-video/</a></p>
      Самое время обзавестись потомством без угрозы здоровью. Кошка полна опыта, умений и может дать котятам все, что нужно без посторонней помощи.
      
     <h3> Зрелый возраст (8-12 лет)</h3>
      Энергия питомца в эти годы идёт на спад. Кот все больше спит, меньше играет, иногда становится агрессивен, так как не хочет, чтобы его трогали и мешали отдыхать.
      <img src="img/old-cat.jpeg" width="493px" height="322px" alt="Зрелый кот">
      <p class='source-img'> Источник:<a class="link" href="https://pulse.mail.ru/article/kogda-vashu-koshku-mozhno-schitat-pozhiloj-4-glavnyh-priznaka-523714473401011076-7126195948557886729/">https://pulse.mail.ru/article/kogda-vashu-koshku-mozhno-schitat-pozhiloj-4-glavnyh-priznaka-523714473401011076-7126195948557886729/</a></p>
      <p>Всё чаще его тревожат разные мелкие болячки. Начинаются первые промахи мимо лотка, рвота из-за сбоя в работе ЖКТ.
      
      Питомец нуждается в более частом осмотре ветеринара.
      
      Однако большинству кошек нужно общество хозяина, и они не прочь полакомиться вкусняшкой.</p>
      
     <h3> Пожилая кошка (от 13 лет)</h3>
      Шерсть кошки тускнеет, зубы выпадают наполовину, а сон становится более глубоким, отрешенным.
      <img src="img/very old.jpeg" width="720px" height="510px" alt="Пожилая кошка">
      <p class='source-img'> Источник:<a class="link" href="https://sport-dog.ru/samaa-staraa-koska-v-mire-foto-i-opisanie/">https://sport-dog.ru/samaa-staraa-koska-v-mire-foto-i-opisanie/</a></p>
      Кошку могут мучать суставные боли, поэтому она не решается запрыгнуть на высоту. Общие движения медленные, энергию старичок расходует экономно.
    
